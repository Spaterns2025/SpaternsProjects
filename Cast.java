class Cast{

//Casting or typecasting
public static void main(String[] args)
{
//1. widening  (smaller datatype into bigger datatype)
double d=6126282;
System.out.println("d="+d);
double d1='6';
System.out.println("d1="+d1);
//2. Narrowing (bigger to smaller)

int a=(int)272.282;
System.out.println("a="+a);
char c=(char)107.828;
System.out.println("c="+c);
}
}